package com.maya.homework.models.responses;

import com.maya.homework.models.TransactionDataItemModel;

public class TransactionListResponse {

    int per_page;
    int current_page;
    String next_page_url;
    String prev_page_url;
    int from;
    int to;
    int total;
    TransactionDataItemModel[] data;

    public int getPer_page() {
        return per_page;
    }

    public void setPer_page(int per_page) {
        this.per_page = per_page;
    }

    public int getCurrent_page() {
        return current_page;
    }

    public void setCurrent_page(int current_page) {
        this.current_page = current_page;
    }

    public String getNext_page_url() {
        return next_page_url;
    }

    public void setNext_page_url(String next_page_url) {
        this.next_page_url = next_page_url;
    }

    public String getPrev_page_url() {
        return prev_page_url;
    }

    public void setPrev_page_url(String prev_page_url) {
        this.prev_page_url = prev_page_url;
    }

    public int getFrom() {
        return from;
    }

    public void setFrom(int from) {
        this.from = from;
    }

    public int getTo() {
        return to;
    }

    public void setTo(int to) {
        this.to = to;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public TransactionDataItemModel[] getData() {
        return data;
    }

    public void setData(TransactionDataItemModel[] data) {
        this.data = data;
    }
}
